  minPlayer = 2,
  maxPlayer = 10,
  rule = "heg_rule",
  logic = heg_getLogic,
  main_mode = "heg_mode",
  is_counted = function(self, room)
    return #room.players >= 6
  end,
  whitelist = {
    "hegemony_cards",
    "hegemony_standard",
    "formation",
    "momentum",
    "transformation",
    "power",
    "strategic_advantage",
