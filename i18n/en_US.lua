# Game rule of Hegemony mode

Hegemony mode (a. k. a. Kingdom Wars) is one of game mode of Sanguosha.
Supports 2~10 players in FreeKill.

## Prepare for start

**Choose characters**

Deal out 6-7 character cards per player. Every player should choose 2
characters with the same nationality, and put them face-down.

The character card that closer to your health card is called as "main
character". The other is called "deputy character".

**Deal out health card**

Every player each takes 1 health card, it indicates you max HP. Max HP is the
sum of health unit of both your characters.

Note: The health unit on character card is called "Yingyang Fish". **When
someone firstly reveals both of his characters, if there is any Yingyang Fish
that doesn't compose to 1 HP, he gets 1 "Yingyang Fish" token card.**
For example, Sima Yi has 1.5 Yingyang Fish, and Zhang Liao has 2 Yingyang
Fish, the sum is 3.5, so his max HP = 3 and he gets 1 "Yingyang Fish" token
when reveals both characters.

When reducing HP, use your main character to lay over your lost HP.

## Proceeding the game

Pick up a player randomly as the start player. Starting from him, every player
eacho proceeds a turn at the reverse-clock order.

Every turn is divided into 6 phases:

Prepare phase -> Judge phase -> Draw phase -> Action phase -> Finish phase

**Prepare phase**

Some skills may be activated at this phase. You may also reveal your hidden
character cards.

**Judge phase**

If there is any card in your judgement area, you must perform judgement for
each of them. If there are multiple cards, judge the last put card first.

**Draw phase**

Draw 2 cards from the top of deck.

**Action phase**

You can use card at your own choice now, but should follow these 2 rules:

1. Can only use 1 Slash per Action phase.
2. Can not use delayed trick to whom has card of the same name in his
  judgement area.

When you use a card, you execute the effect of that card. In common situation,
cards are put into discard pile after being used.

**Discard phase**

Discard your hand cards to your hand card limitation. (Hand card limitation =
your HP)

**Finish phase**

Some skills may be activated at this phase.


## Death

When player's HP reduces to 0 (or lower), he enters brink-of-death. Unless he
uses Peach or Alcohol to himself, or somebody uses Peach to save his life, he
dies.

Dead player should reveal all hidden character cards, discard all cards in his
area (including judgement area), then execute rewards or punishments.

Rewards and punishments:

1. **The player who has confirmed his nationality and kills the same
  nationality must discard all hand cards and equipped cards**;
2. **The player who has confirmed his nationality and kills the different
  nationality draws X cards** (X = # of that nationality, including just
  killed player);
3. Specially, Careerists draw 3 cards after he kills someone.

E. g.: Shu character killed a Wei character, and now there are 2 alive Wei
characters, the Shu character draws 3 cards.

Note: If the killed player doesn't have any revealed character (that is called
"doesn't confirm his nationality"), reveal his characters to examine his
nationality. Players who doesn't confirm his nationality won't do any reward
or punishment after killing players.

## Win and Lose

The goal is related to nationality: **Eliminate all characters with different
nationality from you**.

Special: Careerists need to eliminate all other players.

When all characters have confirmed their nationality, the victory conditions
can be judged.

When only one nationality is alive, they win the game. (A character whose
nationality is not confirmed cannot win the game, even if it is the same
nationality as other survived characters).

## Hide and Reveal

**The hidden character has no skills, gender and nationality**. When hidden
character is about to activate its skill, reveal the card first, then activate
corresponding skill.

Generally, **there are only two moment two reveal hidden character: 1. At
Prepare phase; 2. When activating the skill of this card.** 

E. g. Guo Jia and Sima Yi can reveal when using their skill after suffering
DMG.
Sun Quan and Gan Ning can reveal when activate skill in Action phase.

Note: In online game such as official Sanguosha Online or FreeKill, you need
to "prelight" some passive skills. (Prelight means to click the button of
that skill, and the game will ask you to use skill at appropriate time,
otherwise it won't ask you.)

Specially, **characters with forced-skill can be revealed actively at Action
phase**, such as Zhang Fei (has Roar), Ma Chao (has Horsemanship).

**The first player who reveal his character gets a "Vanguard" token card**.

Player without revealed character has no gender, so weapons and skills that
related to gender can not be activated to him. If there is one revealed card,
his gender becomes to the revealed character. If there are two revealed cards,
his gender becomes to gender of his main character.

Player without revealed character has no nationality. After revealing any
character, his nationality becomes the same as that card (on the top-left
corner of character card), or becomes to Careerist. 

Careerist rule:

**When a player is about to confirm his nationality by revealing character,
if half of all players already confirmed the same nationality, he becomes
Careerist instead**. Give 1 Careerist role card to him. **Careerist is a
nationality that different from any others**, they need to kill all other
players to win the game.

Note: Careerist and another Careerist are considered as different nationality.

E. g.

★ In 6 players or 7 players game, when 4th (also 5th, 6th...) character with
the same nationality appears, he becomes a Careerist.

## Companion

Some of characters are companion. Usually, they are couples or close friends,
such as Liu Bei and Guan Yu, Zhou Yu and Xiao Qiao.

**When reveal both characters at the first time, if they are Companion, you
get a "Companion" token card**.

## Token Cards

**Companion**: In Action phase, can discard "Companion" to draw 2 cards;
can discard "Companion" to view as using Peach.

**Yingyang Fish**: In Action phase, can discard "Yingyang Fish" to draw 1
card; At the start of Discard phase, can discard "Yingyang Fish" to have +2
extra hand card limitation in this turn.

**Vanguard**: In Action phase, can discard "Vanguard" and draw to 4 hand cards,
then observe a hidden character of another player.

## Battle Royal Mode

When there are only 4 alive players (or even fewer), if there is no players
with the same nationality, enter Battle Royal mode at start of next turn.

Battle Royal mode: Peaches (except for virtual card and converted card) can
only be used/played as Slash or Dodge, and can not use for healing.

# 君临天下和势备篇补充规则和术语

## 大小势力 （势备篇）

**大势力**：人数最多且数量不小于2的势力。

**小势力**：若大势力存在，除大势力以外的其他势力。

**大势力角色**：势力为大势力的角色。

**小势力角色**：若大势力角色存在，不为大势力角色的角色。

没有大势力，就不可能有小势力、大势力角色、小势力角色。反之，有大势力，不一定有小势力。

小势力角色还包括未确定势力的角色。即势力为小势力的角色一定是小势力角色，而小势力角色不一定是势力为小势力的角色。

## 合纵 （势备篇）

某些牌上标有“合”标记。

每名角色的出牌阶段限一次，可以选择一项：
1.若你已确定势力，你可以将手牌中有“合”标记的至多三张牌交给与你势力不同的一名角色，摸等量的牌。
2.你可以将手牌中有“合”标记的至多三张牌交给未确定势力的一名角色。

即，出限一、限三张、摸等量。给明将摸牌，给暗将不摸；暗将不能给明将。

## 阵法、围攻、队列、主/副将技 （君临天下·阵）

**阵法技**：在全场存活角色数为4或更多时锁定生效的技能。拥有阵法技的角色可在其准备阶段开始时或出牌阶段发起“阵法召唤”。

**阵法召唤**：由拥有阵法技的角色发起，未确定势力、明置后将会满足此阵法技条件的角色均可明置其一张（满足条件的）武将牌，响应阵法召唤。

**围攻**：一名角色的上家和下家为两名势力相同的角色(且与该角色势力不同)时，该角色被“围攻”，成为“被围攻角色”；该角色的上家和下家成为“围攻角色”；该“围攻角色”和“被围攻角色”处于同一“围攻关系”。

**队列**：连续相邻的若干名(至少2名)势力相同的角色称为同一“队列”。

**主将技**：此武将牌为主将时才能使用的技能。

**副将技**：此武将牌为副武将才能使用的技能。

## 移除 （君临天下·势）

**移除**：将被移除的武将牌置入武将牌堆。被移除武将牌的角色用一张“士兵牌”来代替被移除的武将牌，“士兵牌”的性别、势力与被移除的武将牌相同，但没有技能，该角色没有“士兵牌”所占据的主将/副将。

## 变更 （君临天下·变）

**变更**：（线下）从武将牌堆中连续亮出武将牌，直到亮出一张势力相同的武将牌。将此武将牌作为新的副将（若原先有副将，将副将的原武将牌置入武将牌堆）。每局游戏限一次。

（线上则为从提供的三个同势力武将中选择一个）

## 军令 （君临天下·权）

**军令**：有些技能可以发起军令，发起军令分为（发起者）抽取选择军令和（执行者）询问执行军令。

发起军令的角色随机获得两张军令牌，然后选择其中一张，交给执行军令的角色。

执行军令的角色选择是否执行该“军令”。如果执行，该军令牌的效果生效。

6张军令牌：

军令一：对发起者指定的角色造成1点伤害；

军令二：摸一张牌，然后交给发起者两张牌；

军令三：失去1点体力；

军令四：本回合不能使用或打出手牌且所有非锁定技失效；

军令五：叠置，本回合不能回复体力；

军令六：选择一张手牌和一张装备区里的牌，弃置其余的牌。

